export function moveSvg(g){

    const data = [
        { x: 500, y: 60},
        { x: 500, y: 110},
        { x: 650, y: 110},
        { x: 650, y: 140},
        { x: 700, y: 85},
        { x: 650, y: 30},
        { x: 650, y: 60},
        { x: 500, y: 60},
    ]

    g.setAttribute( "d", changePath(data))  

    function changePath(data){
        data.x 
    } 
}